    r'^(\d{2}\.\d{2}\.\d{4})'          # operation date (group 1)
    r'[ \t]+(\d{2}:\d{2})'             # time            (group 2)
    r'[ \t]+(.+?)'                      # category        (group 3, non-greedy)
    r'[ \t]+([+]?' + _SBER_NUM + r')'  # amount          (group 4)
