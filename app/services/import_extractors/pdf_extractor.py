    def _extract_yandex_credit_statement(
        self,
        *,
        raw_lines: list[str],
        page_stats: list[dict[str, Any]],
        page_count: int,
        contract_number: str | None = None,
        contract_match_reason: str | None = None,
        contract_match_confidence: float | None = None,
        bank_code: str = BANK_CODE_YANDEX,
        account_type_hint: str | None = ACCOUNT_TYPE_CREDIT_CARD,
    ) -> ExtractionResult:
        statement_lines = self._slice_yandex_credit_lines_v2(raw_lines)
        filtered_lines = [line for line in statement_lines if not self._is_service_line(line)]
        parsed_rows, rejected_blocks = self._parse_yandex_credit_rows(filtered_lines)
        tables: list[ExtractedTable] = []

        if parsed_rows:
            tables.append(
                ExtractedTable(
                    name="pdf_transactions",
                    columns=[
                        "date", "posted_date", "description", "amount",
                        "currency", "direction", "balance_after",
                        "account_hint", "counterparty", "raw_type", "source_reference",
                    ],
                    rows=parsed_rows,
                    confidence=0.94 if len(parsed_rows) >= 3 else 0.80,
                    meta={"schema": "normalized_transactions", "parser": "yandex_credit_pdf_v1"},
                )
            )

        if rejected_blocks:
            tables.append(
                ExtractedTable(
                    name="pdf_diagnostics",
                    columns=["raw_block", "diagnostic_reason", "layout_guess"],
                    rows=rejected_blocks[:50],
                    confidence=0.25,
                    meta={"schema": "diagnostics", "parser": "yandex_credit_pdf_v1"},
                )
            )

        if not tables:
            tables.append(self._diagnostics_table(reason="yandex_credit_parse_error"))

        return ExtractionResult(
            source_type=self.source_type,
            tables=tables,
            meta={
                "page_count": page_count,
                "needs_ocr": False,
                "text_based": True,
                "line_count": len(raw_lines),
                "bank_code": bank_code,
                "account_type_hint": account_type_hint,
                "contract_number": contract_number,
                "contract_match_reason": contract_match_reason,
                "contract_match_confidence": contract_match_confidence,
                "filtered_line_count": len(filtered_lines),
                "parsed_transaction_count": len(parsed_rows),
                "rejected_block_count": len(rejected_blocks),
                "layout_counts": {"yandex_credit_block": len(parsed_rows) + len(rejected_blocks)},
                "preview_text": "\n".join(raw_lines[:40]),
                "diagnostics": {
                    "pages": page_stats,
                    "sample_filtered_lines": filtered_lines[:25],
                    "sample_rejected_blocks": rejected_blocks[:5],
                },
            },
        )

    @staticmethod
    def _slice_yandex_credit_lines_v2(raw_lines: list[str]) -> list[str]:
        start_index = 0
        for idx, line in enumerate(raw_lines):
            if line.strip().lower().startswith("\u043e\u043f\u0438\u0441\u0430\u043d\u0438\u0435 \u043e\u043f\u0435\u0440\u0430\u0446\u0438\u0438"):
                start_index = idx + 1
                break
        end_index = len(raw_lines)
        standalone_amount = re.compile(r"^[\d\xa0\s]+,\d{2}\s*\u20bd$")
        for idx in range(start_index, len(raw_lines)):
            line = raw_lines[idx].strip()
            if standalone_amount.match(line):
                end_index = idx
                break
            lowered = line.lower()
            if lowered.startswith("\u0432\u0441\u0435\u0433\u043e \u0440\u0430\u0441\u0445\u043e\u0434\u043d\u044b\u0445 \u043e\u043f\u0435\u0440\u0430\u0446\u0438\u0439") or lowered.startswith("\u0432\u0441\u0435\u0433\u043e \u043f\u0440\u0438\u0445\u043e\u0434\u043d\u044b\u0445 \u043e\u043f\u0435\u0440\u0430\u0446\u0438\u0439"):
                end_index = idx
                break
        return raw_lines[start_index:end_index]

