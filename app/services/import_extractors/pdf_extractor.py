class PdfExtractor(BaseExtractor):
    source_type = "pdf"

    def extract(self, *, filename: str, raw_bytes: bytes, options: dict[str, Any] | None = None) -> ExtractionResult:
        try:
            reader = PdfReader(io.BytesIO(raw_bytes))
        except PdfReadError as exc:
            return ExtractionResult(
                source_type=self.source_type,
                tables=[self._diagnostics_table(reason="yandex_credit_parse_error")],
                meta={
                    "page_count": 0,
                    "needs_ocr": False,
                    "text_based": False,
                    "diagnostics": {"reason": "pdf_read_error", "error": str(exc)},
                },
            )
        except Exception as exc:
            return ExtractionResult(
                source_type=self.source_type,
                tables=[self._diagnostics_table(reason="yandex_credit_parse_error")],
                meta={
                    "page_count": 0,
                    "needs_ocr": False,
                    "text_based": False,
                    "diagnostics": {"reason": "pdf_open_error", "error": str(exc)},
                },
            )

        if getattr(reader, "is_encrypted", False):
            try:
                decrypt_result = reader.decrypt("")
            except Exception as exc:
                decrypt_result = 0
                decrypt_error = str(exc)
            else:
                decrypt_error = None

            if not decrypt_result:
                return ExtractionResult(
                    source_type=self.source_type,
                    tables=[self._diagnostics_table(reason="yandex_credit_parse_error")],
                    meta={
                        "page_count": len(reader.pages),
                        "needs_ocr": False,
                        "text_based": False,
                        "diagnostics": {
                            "reason": "encrypted_pdf",
                            **({"error": decrypt_error} if decrypt_error else {}),
                        },
                    },
                )

        page_texts: list[str] = []
        page_stats: list[dict[str, Any]] = []
        for index, page in enumerate(reader.pages, start=1):
            try:
                text = page.extract_text() or ""
                extract_error = None
            except Exception as exc:
                text = ""
                extract_error = str(exc)
            if text.strip():
                page_texts.append(text)
            page_stat = {"page": index, "text_length": len(text)}
            if extract_error:
                page_stat["extract_error"] = extract_error
            page_stats.append(page_stat)

        full_text = "\n".join(page_texts).strip()
        if not full_text:
            return ExtractionResult(
                source_type=self.source_type,
                tables=[self._diagnostics_table(reason="yandex_credit_parse_error")],
                meta={
                    "page_count": len(reader.pages),
                    "needs_ocr": True,
                    "text_based": False,
                    "diagnostics": {"reason": "no_text_extracted", "pages": page_stats},
                },
            )

        raw_lines = [self._normalize_line(line) for line in full_text.splitlines()]
        raw_lines = [line for line in raw_lines if line]
        contract_number, contract_match_reason, contract_match_confidence = self._extract_contract_number_details(raw_lines)
        statement_account_number, statement_account_match_reason, statement_account_match_confidence = self._extract_statement_account_number_details(raw_lines)

        if self._is_yandex_credit_statement(full_text):
            return self._extract_yandex_credit_statement(
                raw_lines=raw_lines,
                page_stats=page_stats,
                page_count=len(reader.pages),
                contract_number=contract_number,
                contract_match_reason=contract_match_reason,
                contract_match_confidence=contract_match_confidence,
            )

        if self._is_yandex_bank_statement(full_text, raw_lines):
            return self._extract_yandex_bank_statement(
                raw_lines=raw_lines,
                page_stats=page_stats,
                page_count=len(reader.pages),
                contract_number=contract_number,
                contract_match_reason=contract_match_reason,
                contract_match_confidence=contract_match_confidence,
            )

        filtered_lines = [line for line in raw_lines if not self._is_service_line(line)]
        candidate_blocks = self._segment_blocks(filtered_lines)
        parsed_rows: list[dict[str, str]] = []
        rejected_blocks: list[dict[str, str]] = []
        layout_counter: dict[str, int] = {}

        for block in candidate_blocks:
            layout_counter[block.layout] = layout_counter.get(block.layout, 0) + 1
            parsed = self._parse_block(block)
            if parsed:
                parsed_rows.append(parsed)
            else:
                rejected_blocks.append(
                    {
                        "raw_block": "\n".join(block.lines),
                        "diagnostic_reason": "yandex_credit_parse_error",
                        "layout_guess": block.layout,
                    }
                )

        tables: list[ExtractedTable] = []
        if parsed_rows:
            tables.append(
                ExtractedTable(
                    name="pdf_transactions",
                    columns=[
                        "date",
                        "description",
                        "amount",
                        "currency",
                        "direction",
                        "balance_after",
                        "account_hint",
                        "counterparty",
                        "raw_type",
                        "source_reference",
                    ],
                    rows=parsed_rows,
                    confidence=0.9 if len(parsed_rows) >= max(1, len(candidate_blocks) // 2) else 0.72,
                    meta={"schema": "normalized_transactions", "parser": "universal_pdf_pipeline_v2"},
                )
            )

        if rejected_blocks:
            tables.append(
                ExtractedTable(
                    name="pdf_diagnostics",
                    columns=["raw_block", "diagnostic_reason", "layout_guess"],
                    rows=rejected_blocks[:50],
                    confidence=0.25,
                    meta={"schema": "diagnostics", "parser": "universal_pdf_pipeline_v2"},
                )
            )

        if not tables:
            tables.append(self._diagnostics_table(reason="yandex_credit_parse_error"))

        return ExtractionResult(
            source_type=self.source_type,
            tables=tables,
            meta={
                "page_count": len(reader.pages),
                "needs_ocr": False,
                "text_based": True,
                "line_count": len(raw_lines),
                "contract_number": contract_number,
                "contract_match_reason": contract_match_reason,
                "contract_match_confidence": contract_match_confidence,
                "statement_account_number": statement_account_number,
                "statement_account_match_reason": statement_account_match_reason,
                "statement_account_match_confidence": statement_account_match_confidence,
                "filtered_line_count": len(filtered_lines),
                "candidate_block_count": len(candidate_blocks),
                "parsed_transaction_count": len(parsed_rows),
                "rejected_block_count": len(rejected_blocks),
                "layout_counts": layout_counter,
                "preview_text": "\n".join(raw_lines[:40]),
                "diagnostics": {
                    "pages": page_stats,
                    "sample_filtered_lines": filtered_lines[:20],
                    "sample_candidate_blocks": ["\n".join(block.lines[:8]) for block in candidate_blocks[:5]],
                    "sample_rejected_blocks": rejected_blocks[:5],
                },
            },
        )

    @staticmethod
    def _is_yandex_credit_statement(full_text: str) -> bool:
        if "\u0410\u041e \u00ab\u042f\u043d\u0434\u0435\u043a\u0441 \u0411\u0430\u043d\u043a\u00bb" not in full_text:
            return False
        if (
            "\u0412\u044b\u043f\u0438\u0441\u043a\u0430 \u043f\u043e \u0434\u043e\u0433\u043e\u0432\u043e\u0440\u0443" not in full_text
            and "\u0412\u044b\u043f\u0438\u0441\u043a\u0430 \u043f\u043e \u0414\u043e\u0433\u043e\u0432\u043e\u0440\u0443" not in full_text
        ):
            return False
        # Яндекс Дебет (bank/debit account) statements share the same header
        # markers as credit-card statements but have a distinct layout: each
        # transaction block contains a standalone "в НН:НН" time-separator line.
        # Credit-card PDFs do NOT have such lines — the time is embedded
        # elsewhere. Presence of 5+ such lines is a reliable debit-account signal.
        _time_sep_count = full_text.count("\nв ")
        if _time_sep_count >= 5:
            return False
        # "Счёт ЭДС" / "Карта Сумма в валюте" appear only in debit/wallet statements.
        if "\u0421\u0447\u0451\u0442 \u042d\u0414\u0421" in full_text or "\u0421\u0447\u0435\u0442 \u042d\u0414\u0421" in full_text:
            return False
        return (
            "\u041f\u043e\u0433\u0430\u0448\u0435\u043d\u0438\u0435 \u043e\u0441\u043d\u043e\u0432\u043d\u043e\u0433\u043e \u0434\u043e\u043b\u0433\u0430" in full_text
            or "\u041e\u043f\u043b\u0430\u0442\u0430 \u0442\u043e\u0432\u0430\u0440\u043e\u0432 \u0438 \u0443\u0441\u043b\u0443\u0433" in full_text
            or "\u041f\u043e\u0433\u0430\u0448\u0435\u043d\u0438\u0435 \u043f\u0440\u043e\u0446\u0435\u043d\u0442\u043e\u0432" in full_text
        )

