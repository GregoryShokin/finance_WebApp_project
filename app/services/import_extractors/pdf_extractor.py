    def _parse_yandex_credit_rows(
        self, lines: list[str]
    ) -> tuple[list[dict[str, str]], list[dict[str, str]]]:
        # Prepass: split lines where pdfplumber concatenated multiple transactions
        # into a single string. This happens when rows in the PDF table span
        # multiple logical cells and the extractor joins them without a newline.
        # Example of a broken line (3 transactions merged):
        #   "Оплата YANDEX*MARKET 14.02 11 361,00 ₽ 11 361,00 ₽ Оплата YANDEX*MARKET 14.02 11 239,00 ₽ ..."
        # We find all inline transactions in the line and replace the line with
        # N synthetic INLINE-format lines so the main loop handles each separately.
        _CONCAT_SPLIT_RX = re.compile(
            r"(.+?)\s+(\d{2}\.\d{2}\.\d{4})\s+([\d\xa0\s]+,\d{2})\s*\u20bd\s+([\d\xa0\s]+,\d{2})\s*\u20bd",
        )
        expanded: list[str] = []
        for line in lines:
            matches = list(_CONCAT_SPLIT_RX.finditer(line))
            if len(matches) >= 2:
                # Reconstruct each match as a canonical INLINE line so the
                # existing YANDEX_CREDIT_INLINE_RX picks it up on the fast path.
                for m in matches:
                    expanded.append(
                        f"{m.group(1).strip()} {m.group(2)} {m.group(3).strip()} \u20bd {m.group(4).strip()} \u20bd"
                    )
            else:
                expanded.append(line)
        lines = expanded

        rows: list[dict[str, str]] = []
        rejected: list[dict[str, str]] = []
        index = 0

        while index < len(lines):
            # Fast path: single-line transaction — description + date + amount1 + amount2 on one line.
            inline = YANDEX_CREDIT_INLINE_RX.match(lines[index])
            if inline:
                row = self._build_yandex_credit_row(
                    description=inline.group("desc").strip(),
                    date=inline.group("date"),
                    amount_raw=inline.group("amount1"),
                )
                if row:
                    rows.append(row)
                index += 1
                continue

            # Slow path: multi-line transaction — accumulate description lines until date+amount line found.
            desc_parts: list[str] = []
            start_index = index
            found = False
            while index < len(lines):
                line = lines[index]
                m = YANDEX_CREDIT_ROW_RX.match(line)
                if m:
                    description = " ".join(desc_parts).strip()
                    if description:
                        row = self._build_yandex_credit_row(
                            description=description,
                            date=m.group("date"),
                            amount_raw=m.group("amount1"),
                        )
                        if row:
                            rows.append(row)
                    else:
                        rejected.append({
                            "raw_block": line,
                            "diagnostic_reason": "date_amount_line_without_description",
                            "layout_guess": "yandex_credit_block",
                        })
                    index += 1
                    found = True
                    break
                desc_parts.append(line)
                index += 1

            if not found and desc_parts:
                rejected.append({
                    "raw_block": "\n".join(desc_parts),
                    "diagnostic_reason": "description_block_without_date_amount_terminator",
                    "layout_guess": "yandex_credit_block",
                })

        return rows, rejected

    def _build_yandex_credit_row(
        self,
        *,
        description: str,
        date: str,
        amount_raw: str,
    ) -> dict[str, str] | None:
        if not description:
            return None
        amount_norm = self._normalize_amount(amount_raw)
        lower = description.lower()
        if "Р В Р’В Р вЂ™Р’В Р В Р Р‹Р Р†Р вЂљРЎС›Р В Р’В Р В Р вЂ№Р В Р вЂ Р В РІР‚С™Р РЋРІвЂћСћР В Р’В Р вЂ™Р’В Р В Р Р‹Р вЂ™Р’ВР В Р’В Р вЂ™Р’В Р В РІР‚в„ўР вЂ™Р’ВµР В Р’В Р вЂ™Р’В Р В Р’В Р Р†Р вЂљР’В¦Р В Р’В Р вЂ™Р’В Р В РІР‚в„ўР вЂ™Р’В°" in lower or "Р В Р’В Р вЂ™Р’В Р В Р’В Р Р†Р вЂљР’В Р В Р’В Р вЂ™Р’В Р В Р Р‹Р Р†Р вЂљРЎС›Р В Р’В Р вЂ™Р’В Р В РІР‚в„ўР вЂ™Р’В·Р В Р’В Р вЂ™Р’В Р В Р’В Р Р†Р вЂљР’В Р В Р’В Р В Р вЂ№Р В Р’В Р Р†Р вЂљРЎв„ўР В Р’В Р вЂ™Р’В Р В РІР‚в„ўР вЂ™Р’В°Р В Р’В Р В Р вЂ№Р В Р вЂ Р В РІР‚С™Р РЋРІвЂћСћ" in lower:
            direction = "income"
            sign = "+"
        else:
            direction = "expense"
            sign = "-"
        if lower.startswith("\u043e\u0442\u043c\u0435\u043d\u0430 \u043f\u043e \u043e\u043f\u0435\u0440\u0430\u0446\u0438\u0438") or "\u0432\u043e\u0437\u0432\u0440\u0430\u0442" in lower:
            direction = "income"
            sign = "+"
        amount = f"{sign}{amount_norm}"
        raw_type = self._classify_yandex_raw_type(description)
        counterparty = self._extract_counterparty(description)
        return {
            "date": date,
            "posted_date": date,
            "description": description,
            "amount": amount,
            "currency": "RUB",
            "direction": direction,
            "balance_after": "",
            "account_hint": "",
            "counterparty": counterparty,
            "raw_type": raw_type,
            "source_reference": date,
        }

