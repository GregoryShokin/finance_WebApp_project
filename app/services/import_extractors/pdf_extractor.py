    @staticmethod
    def _normalize_contract_candidate(value: str | None) -> str | None:
        if not value:
            return None
        candidate = value.strip(" .,:;")
        if not candidate:
            return None
        if candidate.lower().startswith("\u0438\u0441\u0445"):
            return None
        if re.fullmatch(r"\d{2}\.\d{2}\.\d{4}", candidate):
            return None
        if not any(char.isdigit() for char in candidate):
            return None
        if len(candidate) < 5:
            return None
        return candidate

